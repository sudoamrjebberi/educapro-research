

-- جدول المعلمين
CREATE TABLE teachers (
    id INT PRIMARY KEY AUTO_INCREMENT,
    teacher_code VARCHAR(50) UNIQUE,
    gender VARCHAR(10),
    experience_years VARCHAR(50),
    education_level VARCHAR(50),
    specialization TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_gender (gender),
    INDEX idx_experience (experience_years)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- جدول الاستبيانات
CREATE TABLE surveys (
    id INT PRIMARY KEY AUTO_INCREMENT,
    teacher_id INT,
    submission_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    current_tech_usage VARCHAR(50),
    animation_knowledge VARCHAR(50),
    visual_effectiveness VARCHAR(50),
    FOREIGN KEY (teacher_id) REFERENCES teachers(id) ON DELETE CASCADE,
    INDEX idx_teacher (teacher_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- جدول الإجابات
CREATE TABLE answers (
    id INT PRIMARY KEY AUTO_INCREMENT,
    survey_id INT,
    question_number INT,
    question_category VARCHAR(50),
    question_text TEXT,
    answer_value TEXT,
    answer_type VARCHAR(20),
    FOREIGN KEY (survey_id) REFERENCES surveys(id) ON DELETE CASCADE,
    INDEX idx_category (question_category),
    INDEX idx_survey (survey_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

-- جدول التوصيات
CREATE TABLE recommendations (
    id INT PRIMARY KEY AUTO_INCREMENT,
    teacher_id INT,
    recommendation_text TEXT,
    category VARCHAR(50),
    priority_level INT DEFAULT 1,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (teacher_id) REFERENCES teachers(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

